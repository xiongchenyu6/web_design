<p><b>Who We Are:</b></p>
<p>We are a local startup that wants to make joining and hosting parties easy, simple and fun!</p>
<p><b>Why we do:</b></p>
<p>We know that finding the next hyped party to go next can be a headache for a regular partygoer and so is hosting a good party.</p>
<p>Partystarter aims to be the go-to portal that brings partygoers and hosts together, thus making everyone's lives so much easier.</p>

<p><b>How We Do:</b></p>
<p>With Partystarter, individuals or small companies can effectively manage the registration and ticketing of their parties without any hassle.</p>
<p>To set up a party listing can be done in minutes. Simply register as a member and you can create a new party listing. Your party will be displayed on our pages so you don't have to worry about the attendance for your party! You just have to focus on creating the perfect atmosphere on the day for a good time with your guests.</p>

<p>Have a burning question? Take a look at our <a href="support.php">FAQs</a> or contact us below! We love to hear from you.</p>

