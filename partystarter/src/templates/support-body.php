    <br><br>
    <p><b>How do I create a party listing?</b></p>
    <p>Simply <a href="register.php">signup</a> or <a href="login.php">login</a> as a member and you can click on the
        "create a party" button. Just provide us with the essential details of your party such as the date, location and
        ticket price etc and you are good to go!</p><br>

    <p><b>How much does it cost to sell tickets on Partystarter?</b></p>
    <p>It is absolutely free to list your party! You will get 100% of your ticket sales. Awesome, isn't it?</p><br>

    <p><b>What happens when the tickets for my party are sold out?</b></p>
    <p>Congratulations on reaching full capacity of your party!
        Your listing will be unlisted and there will be not any more sale of tickets for your event. Don't worry about
        party crashers and just focus on hosting the best party!</p>
    <br><br>
    <p><b>For Partygoers</b></p>
    <hr>
    <br><br>
    <p><b>How do I know I have successfully joined a party?</b></p>
    <p>Upon successful payment, you will be redirected to a "successful payment" page. You can also find your tickets
        attached in an email sent to the email address that you have provided us.</p><br>
    <p><b>Why are some of the party tickets more expensive than others?</b></p>
    <p> Party hosts on Partystarter have the freedom to set their own price for their parties.<br>
        Ticket prices may differ due to many reasons such as party location, cost of party supplies etc.</p>
